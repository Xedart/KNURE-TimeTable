import Foundation

// This file contains the color constant for the app.
// Colors values are copied from ChameleonFramework library.

public func FlatYellowDark() -> UIColor {
    return UIColor(hue: 0.11, saturation: 1.00, brightness: 1.00, alpha: 1)
}

public func FlatMint() -> UIColor {
    return UIColor(hue: 0.47, saturation: 0.86, brightness: 0.74, alpha: 1)
}

public func FlatPurpleDark() -> UIColor {
    return UIColor(hue: 0.70, saturation: 0.56, brightness: 0.64, alpha: 1)
}

public func FlatGray() -> UIColor {
    return UIColor(hue: 0.51, saturation: 0.10, brightness: 0.65, alpha: 1)
}

public func FlatSkyBlue() -> UIColor {
    return UIColor(hue: 0.57, saturation: 0.76, brightness: 0.86, alpha: 1)
}

public func FlatWhite() -> UIColor {
    return UIColor(hue: 0.53, saturation: 0.02, brightness: 0.95, alpha: 1)
}

public func FlatGrayDark() -> UIColor {
    return UIColor(hue: 0.51, saturation: 0.10, brightness: 0.55, alpha: 1.0)
}

public func FlatTeal() -> UIColor {
    return UIColor(hue: 0.54, saturation: 0.55, brightness: 0.51, alpha: 1)
}

